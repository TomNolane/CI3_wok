
<h2>Займ на банковский счет</h2>

	Моментальный перевод на банковский счет используют люди не покупающие товары в интернете, и не имеющие на руках банковских карт.
	Применение этого сервиса максимально просто и проходит в три шага:
	<ul>
	  <li>Заполнение заявки</li>
	  <li>Дождаться звонка менеджера</li>
	  <li>Получение денег в банке</li>
	</ul>
	Оформление займа в среднем занимает 15 минут, после чего можно идти получать деньги в банк.